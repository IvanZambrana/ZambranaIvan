public class Main {
    public static void main(String[] args) {
        //Variables
        double base, altura, area;

        //Cálculos
        base = 3;
        altura = 4;
        area = (base * altura) / 2;

        //Mostrar por pantalla
        System.out.println("El área del triángulo cuya base es "
                + base + " cm y cuya altura es "
                + altura + " cm, es igual a "
                + area + " cm^2");
    }
}